data = read.csv("imf-dm-export-20200524.csv")
debt = as.vector(t(as.numeric(data[data$DEBT....of.GDP. == "United Kingdom",2:217])))
Ukdebt = c(debt, c(88.98,87,84,81,83,76),c(88.98,120,117,114,112,113))
# Input dummy variables for two different forecasts
Year = c(1800:2015,2015:2020,2015:2020)
datatype = factor(c(rep(0,216), rep(1,6), rep(2,6)), levels = c(0,1,2), labels = c("Historical Data", "Pre-Crisis Forecast","Post-Crisis Forecast"))
dat = data.frame(Year, Ukdebt, datatype)
rectangle = data.frame(xmin = 2015, xmax = 2020, ymin = -Inf, ymax = Inf)
head(dat)
library(ggplot2)
c = ggplot(dat[101:228,], aes(x = Year, y = Ukdebt)) + geom_line(aes(color = datatype), size = 1.5)
b = c+theme_classic() + labs(x = "Year", y = "% of GDP", title = "UK Public Debt")
a = b + scale_x_continuous(breaks = c(1900,  1920, 1940, 1960, 1980, 2000, 2020), labels = c("1900", "20", "40", "60", "80", "2000", "20")) + scale_y_continuous(limits = c(0,300), breaks = c(0,100,200,300)) + labs(color = "Legend")
a + geom_rect(data = rectangle, aes(xmin = xmin, xmax = xmax, ymin = ymin, ymax = ymax), fill = "yellow", alpha = 0.2, inherit.aes = FALSE)
